
import java.util.Scanner;
class Addition_No
{
	public static void main(String args[])
	{
		
		
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		
		for (int i= 1; i <= b; i++)
		{
            a++;
            }
		
		System.out.println(a);
	}
}